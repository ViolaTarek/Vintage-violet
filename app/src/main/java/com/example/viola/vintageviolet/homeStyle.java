package com.example.viola.vintageviolet;

public class homeStyle {
    private String desc;
    private String url;
    private int id;

    public homeStyle() {
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String imageURL) {
        this.url = imageURL;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
